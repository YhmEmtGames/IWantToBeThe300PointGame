function loadLevel(levelToLoad)
{
  blocks = [];
  enemies = [];
  var num = 0;
  var enemynum = 0;
  for(var o=0;o < levelToLoad.length;o++)
  {
    for (var u=0; u < levelToLoad[o].length;u++)
    {
      if(levelToLoad[o][u] == 1)
      {
        blocks[num] = {
          x:  u*bw,
          y: o*bw
        }
        num++;
      }
      else if (levelToLoad[o][u] == 2)
      {
        goal.x = u*bw;
        goal.y = o*bw;
      }
      else if (levelToLoad[o][u] == 3)
      {
        var enemy1 = new Enemy1();
        enemy1.x = u*bw;
        enemy1.y = o*bw;
        enemies[enemynum] = enemy1;
        enemynum++;
      }
      else if (levelToLoad[o][u] == 4)
      {
        var enemy2 = new Enemy2();
        enemy2.x = u*bw;
        enemy2.y = o*bw;
		enemy2.origy = enemy2.y;
        enemies[enemynum] = enemy2;
        enemynum++;
      }
    }
  }
  initEnemies1();
}
